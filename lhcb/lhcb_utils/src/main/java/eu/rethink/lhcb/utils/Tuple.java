package eu.rethink.lhcb.utils;

public class Tuple<X, Y> {
    public final X x;
    public final Y y;

    public Tuple(X x, Y y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "(" + x + "," + y + ")";
    }

    @Override
    public boolean equals(Object o) {
        if (o == this)
            return true;
        else if (o instanceof Tuple) {
            try {
                return x.equals(((Tuple) o).x) && y.equals(((Tuple) o).y);
            } catch (Exception e) {
                return false;
            }
        } else {
            return false;
        }
    }
}
